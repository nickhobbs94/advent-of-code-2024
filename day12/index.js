function insideShape(x,y,letter) {
  return x >= 0 && y >= 0 && x < width && y < height && grid[y][x] === letter;
}

function hasDirectedBoundary(x,y,dirX,dirY) {
  const letter = grid[y]?.[x];
  return !insideShape(x+dirX, y+dirY, letter);
}

function floodfill(startingStack, insidefn, neighboursgen) {
  const stack = startingStack.slice();
  const visited = {};
  const insideResults = [];
  while (stack.length) {
    const vec = stack.pop();

    if (visited[vec]) continue;
    visited[vec] = true;

    if (!insidefn(vec)) continue;

    insideResults.push(vec);

    neighboursgen(vec).forEach(n => stack.push(n));
  }

  return insideResults;
}

